function drawTree(h) {
    for (var i = 0; i <= h; i++) {
        var star = "";
        for (var j = 1; j <= 1 * i; j++) {
            star += "*";
        }
        console.log("%c%s", "font-size: 20px; color: green",
            star);
    }
}
drawTree(1)

